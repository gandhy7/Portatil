private void selectcodmunicipio() {

        try {

            // hacemos la conexion
            conexion cc = new conexion();
            Connection cn = cc.conectar();
            Statement estado = cn.createStatement();

            // Se realiza la consulta
            String departamentoescogido = (String) jCcodigoDepart.getSelectedItem();
            // JOptionPane.showMessageDialog(null, departamentoescogido);
            ResultSet resultado = estado.executeQuery("SELECT * FROM codmunicipios where departamento = '" + departamentoescogido + "'");

            // Bucle while para la consulta
            jCcodigoMunic.addItem(" ");
            while (resultado.next()) {
                // Comenzamos a rellenar el combobox a partir de la consulta

                jCcodigoMunic.addItem(resultado.getObject("municipio"));

            }
            cn.close();

        } catch (Exception e) {
            System.out.println(e);

        }

    }

