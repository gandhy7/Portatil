    private void jCactividadEcoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCactividadEcoActionPerformed

        try {
            //llamamos la conexion
            conexion cc = new conexion();
            Connection cn = cc.conectar();
            Statement estado = cn.createStatement();

            String coActividad = (String) jCactividadEco.getSelectedItem();

            ResultSet resultado = estado.executeQuery("SELECT * FROM acteconomicas  WHERE  codigo = '" + coActividad + "'  ");

            String descripcion = null;

            while (resultado.next()) {

                descripcion = resultado.getString("descripcion");

            }

            jLdescripcion.setText(descripcion);

        } catch (SQLException ex) {

            System.out.println(ex);

        }

    }//GEN-LAST:event_jCactividadEcoActionPerformed

