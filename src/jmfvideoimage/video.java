    private void jTAcedulaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTAcedulaKeyPressed
        // TODO add your handling code here:

        if (evt.isControlDown() && evt.getKeyCode() == KeyEvent.VK_C) {

            evt.consume(); //cancela la accion
            JOptionPane.showMessageDialog(this, "ctrl + c");

        } else if (evt.isControlDown() && evt.getKeyCode() == KeyEvent.VK_X) {
            evt.consume(); //cancela la accion
            JOptionPane.showMessageDialog(this, "ctrl + x");

        } else if (evt.isControlDown() && evt.getKeyCode() == KeyEvent.VK_V) {
            evt.consume();
            JOptionPane.showMessageDialog(this, "ctrl + v");

        } else if (evt.isAltDown() && evt.getKeyCode() == KeyEvent.VK_ESCAPE) {
            evt.consume();

            JOptionPane.showMessageDialog(this, "escape");
        } else if (evt.isAltDown() && evt.getKeyCode() == KeyEvent.VK_F1) {
            evt.consume();
            JOptionPane.showMessageDialog(this, "f1");
        } else if (evt.isAltDown() && evt.getKeyCode() == KeyEvent.VK_F4) {
            evt.consume();
            JOptionPane.showMessageDialog(this, "f4");
        } else if (evt.getKeyCode() == 18) {
            evt.consume();
            JOptionPane.showMessageDialog(this, "Tecla Alt desactivada");
        }


    }//GEN-LAST:event_jTAcedulaKeyPressed

