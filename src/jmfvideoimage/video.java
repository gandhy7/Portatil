    public void calcula() {
        Calendar calendario = new GregorianCalendar();
        java.util.Date fechaHoraActual = new java.util.Date();

        calendario.setTime(fechaHoraActual);
        ampm = calendario.get(Calendar.AM_PM) == Calendar.AM ? "AM" : "PM";
        if (ampm.equals("PM")) {
            int h = calendario.get(Calendar.HOUR_OF_DAY);
            hora = h > 9 ? "" + h : "0" + h;
        } else {
            hora = calendario.get(Calendar.HOUR_OF_DAY) > 9 ? "" + calendario.get(Calendar.HOUR_OF_DAY) : "0" + calendario.get(Calendar.HOUR_OF_DAY);
        }
        minutos = calendario.get(Calendar.MINUTE) > 9 ? "" + calendario.get(Calendar.MINUTE) : "0" + calendario.get(Calendar.MINUTE);
        segundos = calendario.get(Calendar.SECOND) > 9 ? "" + calendario.get(Calendar.SECOND) : "0" + calendario.get(Calendar.SECOND);
        anio = calendario.get(Calendar.YEAR) > 999 ? "" + calendario.get(Calendar.YEAR) : "0" + calendario.get(Calendar.YEAR);

        mes = calendario.get(Calendar.MONTH) > 999 ? "" + calendario.get(Calendar.MONTH) : "0" + calendario.get(Calendar.MONTH);
        int numCadena = Integer.parseInt(mes);
        tempMes = numCadena + sumaMes;
        String mesok = Integer.toString(tempMes);
        mes = mesok;

        dia = calendario.get(Calendar.DATE) > 9999 ? "" + calendario.get(Calendar.DATE) : "0" + calendario.get(Calendar.DATE);

    }

