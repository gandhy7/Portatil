     //metodo encargado de de hacer la verificacion del usuario para poder ingresar al sistema
    private void verificacionExistencia() {

       try {
            //llamamos la conexion
            conexion cc = new conexion();
            Connection cn = cc.conectar();
            Statement estado = cn.createStatement();


                String nombrepantalla = jTusuario.getText();
                String passwordpantalla = jPassword.getText();


                ResultSet resultado = estado.executeQuery("SELECT * FROM user  WHERE  nombreUsuario = '" + nombrepantalla + "'  AND  password = '" + passwordpantalla + "' ");


                String nombre = "", password = "", tipousuario = "";

                while (resultado.next()) {

                    nombre = resultado.getString("nombreUsuario");
                    password = resultado.getString("password");
                    tipousuario = resultado.getString("tipo");
                }

                // le recuerda al usuario que debe de llenar los campos para hacer la validacion
               if(jPassword.getText().equals("") || jTusuario.getText().equals("") || jTusuario.getText().isEmpty() || jPassword.getText().isEmpty()
                 )

                     {
                       JOptionPane.showMessageDialog(null, "debe llenar todos los campos","Atencion",JOptionPane.WARNING_MESSAGE);

                     }
           else{


                if((nombre.equals(nombrepantalla)) && (password.equals(passwordpantalla))) {


                    //ingreso a la interface principa panel de administrador de sistema
                     Menu mn = new Menu();

                     //String user = jTuser.getText();
                    // String tipo = jTuser.getText();
                     //pasa la variable nombre a la ventana principal al label de usuario
                     mn.pasar((nombre), (tipousuario));
                     mn.setVisible(true);

                    //este dispose cierra la ventana de password automaticamente
                    dispose();

                } else {
                    JOptionPane.showMessageDialog(null, "Usuario o Password no valido");
                }
               }


        } catch (SQLException ex) {
            System.out.println(ex);


}

        }
        
    

